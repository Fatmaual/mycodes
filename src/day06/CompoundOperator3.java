package day06;

public class CompoundOperator3 {
    public static void main(String[] args) {

        int lemons = 25;
        lemons += 10;
        System.out.println("bought 10 more lemons " + lemons);
        lemons -= 3;
        System.out.println( "ate 3 lemons " + lemons);
        lemons /= 2;
        System.out.println(lemons);







    }
}
