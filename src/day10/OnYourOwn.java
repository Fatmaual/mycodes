package day10;

public class OnYourOwn {
    public static void main(String[] args) {
        String doYourOwn = "";
        int doYourNumber = 8;
        switch(doYourNumber){
            case 1:
                System.out.println("I do ");
                doYourOwn = "do";
                break;
            case 2:
                System.out.println("I do ");
                doYourOwn ="I do ";
                break;
            case 3:
                System.out.println("I do ");
                doYourOwn = "I do";
                break ;
            case 4 :
                System.out.println("I do");
                doYourOwn ="I do";
                break;




        }


    }
}
