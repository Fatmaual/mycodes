package day02;

public class VariableAndConcatenation {

    public static void main(String [] args){

        int MyBirthYear = 1979 ;
        int MyAge = 40 ;
        int MyFamilyNumber = 6 ;
        int MyAreaCode = 30004 ;
        int MyDesiredSalary = 80000 ;

        System.out.println("My birth year is " + MyBirthYear ) ;
        System.out.println("My age is " + MyAge) ;
        System.out.println( "My family member count is " + MyFamilyNumber ) ;
        System.out.println( "My area code is " + MyAreaCode ) ;
        System.out.println("My desired salary is " + MyDesiredSalary) ;


    }






}